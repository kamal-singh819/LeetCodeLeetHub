class Solution{
    public:
        int modulo(string s,int m)
        {
            int n = s.length();
            int rem = 0;
            for(int i=0; i<n; i++){
                rem = rem*2 + (s[i] - '0');
                rem = rem%m;
            }
            return rem;
        }
};

